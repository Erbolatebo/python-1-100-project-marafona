from_currency = input("Из какой валюты: ").upper()
to_currency = input("В какую валюту: ").upper()
amount = float(input("Сумма: "))

rates = {
    "USD": 1.0,        # Доллар США
    "EUR": 0.91,       # Евро
    "KZT": 460.0,      # Казахстанский тенге
    "RUB": 90.0,       # Российский рубль
    "GBP": 0.77,       # Британский фунт стерлингов
    "JPY": 144.5,      # Японская иена
    "CNY": 7.25,       # Китайский юань
    "TRY": 32.7,       # Турецкая лира
    "UZS": 12500.0,    # Узбекский сум
    "KGS": 89.5,       # Киргизский сом
    "AED": 3.67,       # Дирхам ОАЭ
    "INR": 83.0,       # Индийская рупия
    "CHF": 0.89,       # Швейцарский франк
    "CAD": 1.32,       # Канадский доллар
    "AUD": 1.48,       # Австралийский доллар
    "NOK": 10.5,       # Норвежская крона
    "SEK": 10.8,       # Шведская крона
    "PLN": 4.0,        # Польский злотый
    "KRW": 1300.0,     # Южнокорейская вона
    "BRL": 5.1         # Бразильский реал
}

# Проверка, что введенные валюты существуют в словаре
if from_currency not in rates or to_currency not in rates:
    print("Ошибка: Одна из валют не поддерживается!")
else:
    # Конвертация: сначала в USD, затем в целевую валюту
    converted = (amount / rates[from_currency]) * rates[to_currency]
    print(f"{amount:.2f} {from_currency} = {converted:.2f} {to_currency}")